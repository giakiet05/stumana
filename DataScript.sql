BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Assets" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Assets" PRIMARY KEY,
    "MinAge" INTEGER NOT NULL,
    "MaxAge" INTEGER NOT NULL,
    "MaxCapacity" INTEGER NOT NULL,
    "ScoreToPass" REAL NOT NULL,
    "YearId" TEXT NOT NULL,
    CONSTRAINT "FK_Assets_SchoolYears_YearId" FOREIGN KEY ("YearId") REFERENCES "SchoolYears" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Classrooms" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Classrooms" PRIMARY KEY,
    "Name" TEXT NULL,
    "GradeId" TEXT NOT NULL,
    "YearId" TEXT NOT NULL,
    CONSTRAINT "FK_Classrooms_Grades_GradeId" FOREIGN KEY ("GradeId") REFERENCES "Grades" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Classrooms_SchoolYears_YearId" FOREIGN KEY ("YearId") REFERENCES "SchoolYears" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Grades" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Grades" PRIMARY KEY,
    "Name" TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS "SchoolYears" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_SchoolYears" PRIMARY KEY,
    "StartYear" INTEGER NOT NULL,
    "EndYear" INTEGER NOT NULL
);
CREATE TABLE IF NOT EXISTS "ScoreTypes" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_ScoreTypes" PRIMARY KEY,
    "Name" TEXT NOT NULL,
    "Coefficient" REAL NOT NULL,
    "YearId" TEXT NOT NULL,
    CONSTRAINT "FK_ScoreTypes_SchoolYears_YearId" FOREIGN KEY ("YearId") REFERENCES "SchoolYears" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Scores" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Scores" PRIMARY KEY,
    "Value" REAL NOT NULL,
    "Attempt" INTEGER NOT NULL,
    "StudentAssignmentId" TEXT NOT NULL,
    "SubjectScoreTypeId" TEXT NOT NULL,
    CONSTRAINT "FK_Scores_StudentAssignments_StudentAssignmentId" FOREIGN KEY ("StudentAssignmentId") REFERENCES "StudentAssignments" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Scores_SubjectScoreTypes_SubjectScoreTypeId" FOREIGN KEY ("SubjectScoreTypeId") REFERENCES "SubjectScoreTypes" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "StudentAssignments" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_StudentAssignments" PRIMARY KEY,
    "Semester" INTEGER NOT NULL,
    "Conduct" TEXT NOT NULL,
    "Absence" INTEGER NOT NULL,
    "Comment" TEXT NOT NULL,
    "StudentId" TEXT NOT NULL,
    "ClassroomId" TEXT NOT NULL,
    CONSTRAINT "FK_StudentAssignments_Classrooms_ClassroomId" FOREIGN KEY ("ClassroomId") REFERENCES "Classrooms" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_StudentAssignments_Students_StudentId" FOREIGN KEY ("StudentId") REFERENCES "Students" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Students" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Students" PRIMARY KEY,
    "Name" TEXT NOT NULL,
    "Gender" TEXT NOT NULL,
    "Address" TEXT NOT NULL,
    "Birthday" datetime NOT NULL,
    "Phone" TEXT NOT NULL,
    "Email" TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS "SubjectScoreTypes" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_SubjectScoreTypes" PRIMARY KEY,
    "ScoreTypeId" TEXT NOT NULL,
    "SubjectId" TEXT NOT NULL,
    "Amount" INTEGER NOT NULL,
    CONSTRAINT "FK_SubjectScoreTypes_ScoreTypes_ScoreTypeId" FOREIGN KEY ("ScoreTypeId") REFERENCES "ScoreTypes" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_SubjectScoreTypes_Subjects_SubjectId" FOREIGN KEY ("SubjectId") REFERENCES "Subjects" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Subjects" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Subjects" PRIMARY KEY,
    "Name" TEXT NOT NULL,
    "ScoreToPass" REAL NOT NULL,
    "YearId" TEXT NOT NULL,
    "GradeId" TEXT NOT NULL,
    CONSTRAINT "FK_Subjects_Grades_GradeId" FOREIGN KEY ("GradeId") REFERENCES "Grades" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Subjects_SchoolYears_YearId" FOREIGN KEY ("YearId") REFERENCES "SchoolYears" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Users" (
    "Id" TEXT NOT NULL CONSTRAINT "PK_Users" PRIMARY KEY,
    "Username" TEXT NOT NULL,
    "Password" TEXT NOT NULL,
    "Email" TEXT NOT NULL
);

